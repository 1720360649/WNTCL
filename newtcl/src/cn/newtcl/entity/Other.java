package cn.newtcl.entity;

public class Other {
	
	private String qid = null;
	private String qtoken = null;
	private String qname = null;
	private String qavatar = null;
	
	public String getQid() {
		return qid;
	}
	public void setQid(String qid) {
		this.qid = qid;
	}
	public String getQtoken() {
		return qtoken;
	}
	public void setQtoken(String qtoken) {
		this.qtoken = qtoken;
	}
	public String getQname() {
		return qname;
	}
	public void setQname(String qname) {
		this.qname = qname;
	}
	public String getQavatar() {
		return qavatar;
	}
	public void setQavatar(String qavatar) {
		this.qavatar = qavatar;
	}
}
